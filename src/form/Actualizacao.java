     public void preencherDadosFuncionario() {
        ProdutosDAO pd = new ProdutosDAO();
        DefaultTableModel modelo = (DefaultTableModel) tabelaResultados.getModel();
        try {

            ArrayList<CadastroProdutos> produtos = pd.ListarTodos();
            modelo.setRowCount(0);
            produtos.forEach((CadastroProdutos cp) -> {
                modelo.addRow(new Object[]{
                   cp.getId(),cp.getNomeProduto(), cp.getMarca(), cp.getAnoFabrico(), cp.getReferenciaPeca(), cp.getModelo(), cp.getQuantidade(),
                    cp.getValorDeVenda(), cp.getValorDeCompra()});
                
            });

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
