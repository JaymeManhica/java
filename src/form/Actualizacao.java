            txtNome.setText(tabelaResultados.getValueAt(tabelaResultados.getSelectedRow(), 1).toString());
            txtReferencia.setText(tabelaResultados.getValueAt(tabelaResultados.getSelectedRow(), 4).toString());
            txtValorVenda.setText(tabelaResultados.getValueAt(tabelaResultados.getSelectedRow(), 8).toString());
